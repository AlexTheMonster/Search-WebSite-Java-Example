package ss.classes.dao;

public class SSDAOException extends Exception {

    public SSDAOException() {
    }

    public SSDAOException(String arg) {
        super(arg);
    }

    public SSDAOException(Throwable arg) {
        super(arg);
    }

    public SSDAOException(String arg, Throwable arg1) {
        super(arg, arg1);
    }
}
